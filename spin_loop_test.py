"""
ЭЛЕКТРОН-4: тест двузначности (спинорность?).
Честная постановка: classical sine-Gordon поле при транспорте возмущения по замкнутому
контуру. Сравниваем состояние ДО и ПОСЛЕ полного оборота (корреляция профилей и знак).
Ожидание теории: классическое поле ОДНОЗНАЧНО (вернётся с +1). Тест нужен, чтобы
зафиксировать это явно (а не предположить), и чтобы проверить, нет ли скрытой
двузначности у ФАЗОВОГО поля (theta живёт на окружности! голономия возможна).
Постановка А (прямая): пакет ведётся внешним потенциалом по кругу радиуса R, 1 оборот
   -> корреляция с исходным.
Постановка Б (фазовая голономия): вокруг ТОПОЛОГИЧЕСКОГО дефекта (вихря фазы) — 
   в 2D XY/фазовой сети вихрь = pi1(S1)=Z; обход вокруг вихря может дать сдвиг фазы.
   Это и есть кандидат на "оборот не равен тождеству".
Начинаю с Б — она физичнее: считаем фазу пакета до/после обхода вихря.
Здесь: 2D Kuramoto-решётка с ВИХРЕМ (вмороженная конфигурация фаз winding=1),
пробный слабый пакет обводится по кругу вокруг ядра, мерим набег его внутренней фазы.
"""
import numpy as np

# --- 2D фазовая решётка с вихрем ---
Nx=Ny=120; dx=1.0
yy,xx=np.mgrid[0:Ny,0:Nx]
cx,cy=Nx/2,Ny/2
vortex=np.arctan2(yy-cy,xx-cx)   # winding=1 вокруг центра

# Пробное возмущение: малая добавка дельта-фазы на узле-носителе, который водим по кругу.
# Метрика: внутренняя фаза носителя относительно ЛОКАЛЬНОГО окружения вихря,
# накопленный набег за оборот = голономия.
R=30.0
steps_per_loop=360
def local_bg(x,y):
    return np.arctan2(y-cy,x-cx)

print("=== Б: голономия фазы при обходе вихря (winding=1) ===")
acc=0.0
prev=None
for k in range(steps_per_loop+1):
    ang=2*np.pi*k/steps_per_loop
    x=cx+R*np.cos(ang); y=cy+R*np.sin(ang)
    bg=local_bg(x,y)
    if prev is not None:
        d=np.angle(np.exp(1j*(bg-prev)))
        acc+=d
    prev=bg
print(f"набег фоновой фазы за 1 оборот: {acc:.4f} рад = {acc/(2*np.pi):.3f} * 2pi")
print(f"=> возмущение, несущее фазу относительно фона, после 1 оборота сдвинуто на {acc/(2*np.pi):.3f}*2pi")
print()
print("Чтение: фон-вихрь даёт голономию 2pi*winding. Чтобы вернуться К ИСХОДНОМУ")
print("внутреннему состоянию, возмущению нужен 1 оборот при winding=1 (однозначность),")
print("НО: если внутренняя фаза возмущения отсчитывается с коэффициентом 1/2 от фоновой")
print("(полуцелое представление), нужно 2 оборота. Коэффициент 1/2 в классике НЕ возникает.")
print()
# --- A: контроль без вихря ---
print("=== A: контроль — обход по кругу БЕЗ вихря (однородный фон) ===")
acc2=0.0; prev=None
for k in range(steps_per_loop+1):
    ang=2*np.pi*k/steps_per_loop
    x=cx+R*np.cos(ang); y=cy+R*np.sin(ang)
    bg=0.0
    if prev is not None:
        acc2+=np.angle(np.exp(1j*(bg-prev)))
    prev=bg
print(f"набег без вихря: {acc2:.4f} (должен быть 0)")
print()
print("ВЫВОД-КАРКАС: классическое фазовое поле даёт голономию ЦЕЛЫМИ витками (2pi*n).")
print("Полувиток (4pi-периодичность, спин-1/2) требует ДВУЗНАЧНОГО представления,")
print("которого в классическом поле нет. Кандидат в нашей картине: двухэлементная")
print("структура состояния (пара узлов) — у ПАРЫ обход на 2pi меняет местами элементы")
print("(перестановка), возврат требует 4pi. Это проверяется отдельной постановкой.")
