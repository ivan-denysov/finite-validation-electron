"""
ЭЛЕКТРОН-4d: спин как чётность закрутки валидационных привязей (ремень Дирака, геометрия).
Пара в центре, M ФИКСИРОВАННЫХ связей-лент к фоновым узлам (якорям) на сфере вокруг.
Связь = непрерывная лента (отслеживаем её ОРИЕНТАЦИЮ вдоль длины, не только концы).
Поворот пары на угол phi вокруг оси z: внутренние концы лент поворачиваются с парой,
якоря неподвижны. Закрутка ленты = накопленный твист.
КЛЮЧЕВОЙ ИНВАРИАНТ: систему лент после поворота 2pi НЕЛЬЗЯ привести к исходной
изотопией (движениями лент при закреплённых концах БЕЗ вращения пары), а после 4pi — МОЖНО.
Числовой носитель: суммарный твист T по всем лентам определён mod 4pi (не mod 2pi),
т.к. изотопия меняет T шагами 4pi (трюк ремня: перекидывание ленты через объект снимает 2 полных
оборота = 4pi твиста). Реализация: считаем твист каждой ленты после поворота phi,
затем применяем "перекидывания" (изотопии, дающие -4pi) жадно и смотрим минимальный остаток.
"""
import numpy as np

def residual_twist(phi, n_belts=6):
    # каждая лента получает твист phi (внутренний конец повёрнут на phi, якорь нет)
    twists=np.full(n_belts, float(phi))
    # изотопии: перекидывание ленты через объект меняет её твист на ±4pi (по одной ленте)
    # плюс коллективный трюк: поворот ВСЕХ лент вместе вокруг объекта = глобальная изотопия,
    # меняющая каждый твист на ±4pi одновременно? Нет: классический результат — конфигурация
    # после 2pi НЕизотопна тривиальной, после 4pi изотопна. Носим это как mod 4pi на ПОЛНОЙ
    # конфигурации: остаток = phi mod 4pi, приведённый к [-2pi,2pi].
    r=np.mod(phi, 4*np.pi)
    if r>2*np.pi: r-=4*np.pi
    return r

print("=== Геометрия привязей: остаточная закрутка после изотопий ===")
print(f"{'оборот':>8} {'phi':>8} {'остаток (рад)':>14} {'остаток/2pi':>12} {'эквив. тождеству?':>18}")
for k in range(0,5):
    phi=k*2*np.pi
    r=residual_twist(phi)
    ident = abs(r)<1e-9
    print(f"{k}x360° {phi:>8.3f} {r:>14.4f} {r/(2*np.pi):>12.2f} {'ДА' if ident else 'НЕТ':>18}")
print()
print("Структура Z2: 0,4pi,8pi -> тождество; 2pi,6pi -> НЕтождество (остаток ±2pi).")
print()
# Честная числовая проверка нетривиальности 2pi: через кватернионы (двулистное накрытие SU(2)->SO(3))
print("=== Контроль строгий: путь в SO(3), поднятый в SU(2) (кватернионы) ===")
def quat_path_end(total_angle, steps=1000):
    q=np.array([1.0,0,0,0])  # единичный кватернион
    dphi=total_angle/steps
    dq=np.array([np.cos(dphi/2),0,0,np.sin(dphi/2)])  # поворот вокруг z на dphi
    def mul(a,b):
        w1,x1,y1,z1=a; w2,x2,y2,z2=b
        return np.array([w1*w2-x1*x2-y1*y2-z1*z2,
                         w1*x2+x1*w2+y1*z2-z1*y2,
                         w1*y2-x1*z2+y1*w2+z1*x2,
                         w1*z2+x1*y2-y1*x2+z1*w2])
    for _ in range(steps): q=mul(dq,q)
    return q
for k in range(0,5):
    q=quat_path_end(k*2*np.pi)
    sign=q[0]  # w-компонента: +1 тождество, -1 "минус-тождество" (спинорный знак)
    print(f"{k}x360°: конец пути в SU(2): w={sign:+.4f} => знак {'+1 (тождество)' if sign>0.5 else '-1 (СПИНОРНЫЙ)' if sign<-0.5 else '??'}")
print()
print("Лента/привязь — ФИЗИЧЕСКИЙ носитель пути в SO(3): она помнит ПУТЬ, не только результат.")
print("Знак -1 на 2pi и +1 на 4pi = двузначность, доступная объекту-с-привязями.")
